public class displaying {
    public static void main(String[] args) {
        // DISPLAYING USING FOR LOOP
        int marks[] = { 45, 70, 89, 36, 88 };
        System.out.println(marks.length);
        for (int i = 0; i < marks.length; i++) {
            System.out.println(marks[i]);
        }
    }
}
